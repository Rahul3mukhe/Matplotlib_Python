x = [10,20,30,40,50,60,70,120]

plt.boxplot(x,labels = ["red candle"],whis = 2)
plt.show()

x = [10,20,30,40,50,60,70,120]

plt.boxplot(x,labels = ["red candle"],sym = 'g+')
plt.show()

