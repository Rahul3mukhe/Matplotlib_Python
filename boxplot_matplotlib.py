import matplotlib.pyplot as plt

x = [10,20,30,40,50,60,70]

plt.boxplot(x)
plt.show()

plt.boxplot(x,notch = True)
plt.show()

plt.boxplot(x,vert= False)
plt.show()

plt.boxplot(x,widths = 0.8)
plt.show()

plt.boxplot(x,labels = ["redcandle"],showmeans = True)
plt.legend()
plt.show()

x = [10,20,30,40,50,60,70,120]

